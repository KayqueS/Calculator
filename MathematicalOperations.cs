using System;

namespace Calculator
{
    class MathematicalOperations
    {
        public int A;
        public int B;

        public int Sum()
        {
            return A + B;
        }

        public int Sub()
        {
            return A - B;
        }
        public int Multi()
        {
            return A * B;
        }
        public int Div()
        {
            return A / B;
        }
        public int Mod()
        {
            return A % B;
        }
    }
}
